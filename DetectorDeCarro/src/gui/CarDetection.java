     class DaemonThread implements Runnable {

        protected volatile boolean runnable = false;
        
        
        @Override
        public void run() {
            synchronized (this) {
                while (runnable) { 
                    
                    if (webSource.grab()) {
                        try {
                            webSource.retrieve(frame);
                            Graphics g = jPanel1.getGraphics();
                            carDetector.detectMultiScale(frame, carDetections);
                            //Cria o retangulo quando identifica o carro
                            int i =0;
                            for (Rect rect : carDetections.toArray()) {
                                Core.rectangle(frame, new Point(rect.x, rect.y), new Point(rect.x + rect.width, rect.y + rect.height),
                                        new Scalar(0,255,0));
                                //pega qtd de carros
                                ++i;
                                
                            }
                         if(i==1){
                            try {
                                        URL myURL = new URL("http://192.168.0.104:8231/vraptor4/vaga/update/1");
                                        myURL.openStream();
                                        

                                    }  
                                    catch (IOException e) {   
                                        // openConnection() failed
                                        System.out.println("Open URL");
                                        // ...
                                    }
                         }else{
                              try {
                                        URL myURL = new URL("http://192.168.0.104:8231/vraptor4/vaga/update/0");
                                        myURL.openStream();
                                        

                                    } 

                                    catch (IOException e) {   
                                        // openConnection() failed
                                        System.out.println("Open URL");
                                        // ...
                                    }
                         }
                          
                            Highgui.imencode(".bmp", frame, mem);
                            Image im = ImageIO.read(new ByteArrayInputStream(mem.toArray()));
                            BufferedImage buff = (BufferedImage) im;
                            
                            if (g.drawImage(buff, 0, 0, getWidth(), getHeight()-150 , 0, 0, buff.getWidth(), buff.getHeight(), null)) {
                                // Quando a camera para de capturar aparece "pausado" 
                                if (runnable == false) {
                                    System.out.println("Pausado ..... ");
                                    this.wait();
                                }
                            }
                        } catch (Exception ex) {
                            System.out.println("Error");
                        }
                    }
                    
                }
            }
        }
    }

